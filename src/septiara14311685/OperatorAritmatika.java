public class OperatorAritmatika {
  public int nilaihasil;
 
 int penjumlahan (int x,int y){
   nilaihasil=x+y;
   return nilaihasil;
   }
 
 int pengurangan (int x,int y){
   nilaihasil=x-y;
   return nilaihasil;
   }
 int perkalian (int x,int y){
   nilaihasil=x*y;
   return nilaihasil;
   }
 double pembagian (double x,double y){
   nilaihasil= (int) (x/y);
   return nilaihasil;
 }

   
}
